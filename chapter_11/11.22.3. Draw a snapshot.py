a = [1, 2, 3, 4]
b = a[:]
b[0] = 5

################-SNAPSHOT-####################
#                                            #
#   a ---> object a             [1, 2, 3, 4] #
#   b ---> object b (a's copy)  [1, 2, 3, 4] #
#                                            #
#   When b[0] = 5                            #
#   a ---> object a [1, 2, 3, 4]             #
#   b ---> object b [5, 2, 3, 4]             #
#                                            #
##############################################

print(a, b)